class PixelArt:
    def __init__(self, msg: str) -> None:
        self.msg = msg
        self.backgroundChar = ' '
        self.foregroundChar = '+'

        self.pixels = [[], [], [], [], [], [], []]
        for character in msg:
            pixel = PixelArtDict[character]

            for rowIndex, row in enumerate(pixel):
                self.pixels[rowIndex].extend(row)
        return

    def write_pixels_to_file(self, fileName: str) -> None:
        with open(fileName, 'w+') as f:
            for row in self.pixels:
                for pixel in row:
                    f.write(self.foregroundChar if pixel else self.backgroundChar)
                f.write('\n')
        return